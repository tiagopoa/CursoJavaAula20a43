package aula34.labs;

public class ConversaoDeUnidadeDeTempo {

	public static double converterDeMinutoParaSegundo(double num) {
		return num * 60;
	}
	
	public static double converterDeHoraParaMinuto(double num) {
		return num * 60;
	}
	
	public static double converterDeDiaParaHora(double num) {
		return num * 24;
	}
	
	public static double converterDeSemanaParaDia(double num) {
		return num * 7;
	}
	
	public static double converterDeMesParaDia(double num) {
		return num * 30;
	}
	
	public static double converterDeAnoParaDia(double num) {
		return num * 365.25;
	}
	
}
