package aula34.labs;

public class ConversaoDeUnidadesDeVolume {

	public static double converterLitroParaCentimetroCubico(double num) {
		return num * 1000;
	}
	
	public static double converterMetroCubicoParaLitro(double num) {
		return num * 1000;
	}
	
	public static double converterMetroCubicoParaPeCubico(double num) {
		return num * 35.32;
	}
	
	public static double converterGalaoAmericanoParaPolegadasCubicas(double num) {
		return num * 231;
	}
	
	public static double converterGalaoAmericanoParaLitro(double num) {
		return num * 3.785;
	}
	
}
