package aula34.labs;

public class ConversaoDeUnidadesDeArea {
	
	public static double converteMetroQuadradoParaPes(double num) {
		return num * 10.76;
	}
	
	public static double convertePeQuadradoParaCentimetroQuadrado(double num) {
		return num * 929;
	}
	
	public static double converteMilhaQuadradaParaAcres(double num) {
		return num * 640;
	}
	
	public static double converteAcreParaPesQuadrados(double num) {
		return num * 43560;
	}	

}
